    private void btnPromeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromeniActionPerformed
        try {
            Zaposleni z = new Zaposleni();
            z.setId(zaposleni.getId());
            z.setIme(txtIme.getText());
            z.setPrezime(txtPrezime.getText());
            z.setEmail(txtEmail.getText());
            if(cbPromeniSifru.isSelected()) {
                z.setSifra(new String(txtNovaSifra.getPassword()));
            }
            NetworkResponse resp = RequestManager.sendRequest(new NetworkRequest("zaposleni/vrati_sve", List.of(new KriterijumDescriptor(Zaposleni.class, "id", "=", zaposleni.getId()))));
            if (!resp.success) {
                JOptionPane.showMessageDialog(null, "Došlo je do greške pri promeni podataka. Pokušajte ponovo.", "Greška", JOptionPane.ERROR_MESSAGE);
                return;
            }
            List<Zaposleni> l = (List<Zaposleni>) resp.payload;
            if (l.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Došlo je do greške pri identifikaciji. Molimo ulogujte se ponovo.", "Greška", JOptionPane.ERROR_MESSAGE);
                dispose();
                parent.dispose();
                LoginView v = new LoginView();
                LoginModel m = new LoginModel();
                LoginKontroler p = new LoginKontroler(v, m);
                v.setVisible(true);
                return;
            }
            Zaposleni vracen = l.getFirst();
            if( cbPromeniSifru.isSelected() && (!vracen.getSifra().equals(zaposleni.getSifra()) 
                    || !vracen.getSalt().equals(zaposleni.getSalt()) 
                    || !vracen.getSifra().equals(PasswordHasher.hash(new String(txtStaraSifra.getPassword()), Base64.getDecoder().decode(zaposleni.getSalt()))))) {
                JOptionPane.showMessageDialog(null, "Uneta stara šifra nije ispravna. Pokušajte ponovo.", "Greška", JOptionPane.ERROR_MESSAGE);
                return;
            }

            NetworkResponse resp2 = RequestManager.sendRequest(new NetworkRequest("zaposleni/promeni", z));
            if (!resp2.success) {
                JOptionPane.showMessageDialog(null, resp2.responseMessage, "Greška", JOptionPane.ERROR_MESSAGE);
                return;
            }
            dispose();
            zaposleni.setIme(z.getIme());
            zaposleni.setPrezime(z.getPrezime());
            zaposleni.setEmail(z.getEmail());
            ((GlavniMeniView)parent).setUlogovanoIme(zaposleni.getIme() + " " + zaposleni.getPrezime());
            JOptionPane.showMessageDialog(null, "Uspešno promenjeni podaci.", "Uspeh", JOptionPane.INFORMATION_MESSAGE);
        }
        catch(ClassCastException ex) {
            SimpleLogger.log(LogLevel.LOG_ERROR, "Pozvan iz neocekivanog roditelja");
        }
        catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Došlo je do greške pri promeni podataka. Molimo pokušajte kasnije.", "Greška", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPromeniActionPerformed

