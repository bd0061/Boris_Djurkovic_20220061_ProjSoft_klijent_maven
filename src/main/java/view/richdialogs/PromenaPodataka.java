    private void proveriPolja() {
        if (txtIme.getText().isEmpty()
                || txtPrezime.getText().isEmpty()
                || txtEmail.getText().isEmpty()
                || (cbPromeniSifru.isSelected() && ((new String(txtStaraSifra.getPassword()).isEmpty()) || (new String(txtNovaSifra.getPassword()).isEmpty())))) {
            btnPromeni.setEnabled(false);
        } else {
            btnPromeni.setEnabled(mailValid && (!cbPromeniSifru.isSelected() || duzinaValid));
        }
    }

    private void proveriMejl() {
        Matcher m = p.matcher(txtEmail.getText());
        mailValid = m.matches();
        if (mailValid || txtEmail.getText().isEmpty()) {
            lblMailError.setVisible(false);
        } else {
            lblMailError.setVisible(true);
        }
    }

    private void proveriDuzina() {
        duzinaValid = !cbPromeniSifru.isSelected() || txtNovaSifra.getPassword().length == 0 || txtNovaSifra.getPassword().length >= 8;
        lblSifraError.setVisible(!duzinaValid);
    }

