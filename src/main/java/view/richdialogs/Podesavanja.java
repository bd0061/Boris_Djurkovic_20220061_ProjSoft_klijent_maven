    private void btnPotvrdiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPotvrdiActionPerformed
        try {
            var ui = map.get(comboBoxTeme.getSelectedItem());
            if(ui.getName().equals(UIManager.getLookAndFeel().getName())) {
                dispose();
                return;
            }
            UIManager.setLookAndFeel(ui.getClassName());
            dispose();
            //propagiramo promenu ka svim prozorima (swing po defaultu updatuje samo trenutni i njegovu decu)
            for (Window window : Window.getWindows()) {
                SwingUtilities.updateComponentTreeUI(window);
            }
            JOptionPane.showMessageDialog(null, "Uspešno promenjena tema.", "Uspeh", JOptionPane.INFORMATION_MESSAGE);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Došlo je do greške pri promeni teme.", "Greška", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPotvrdiActionPerformed

