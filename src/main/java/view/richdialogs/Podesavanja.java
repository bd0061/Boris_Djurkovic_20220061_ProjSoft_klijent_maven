    private void popuniComboBox() {
        UIManager.LookAndFeelInfo[] lafs = UIManager.getInstalledLookAndFeels();
        LookAndFeel currentLaf = UIManager.getLookAndFeel();
        System.out.println(currentLaf.getName());
        for (var laf : lafs) {
            comboBoxTeme.addItem(laf.getName());
            map.put(laf.getName(), laf);
            if (laf.getName().equals(currentLaf.getName())) {

                comboBoxTeme.setSelectedItem(laf.getName());
            }
        }
    }
